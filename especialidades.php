<fontered>Missão do Conselho Federal de Odontologia</fontered><br /><br />

O Conselho Federal de Odontologia e os 27 Conselhos Regionais de Odontologia, criados pela Lei nº 4.324, de 14 de abril de 1964 e, posteriormente, instituídos pelo Decreto nº 68.704, de 3 de junho de 1971, formam em seu conjunto uma Autarquia.<br /><br />

Tanto o CFO quanto cada CRO são dotados de personalidade jurídica de direito público, com autonomia administrativa e financeira.<br /><br />

Sua principal finalidade é a supervisão da ética odontológica em todo o território nacional, cabendo-nos zelar e trabalhar pelo bom conceito da profissão e dos que a exercem legalmente. Para cumprir essa missão, o CFO legisla por meio de Atos Normativos, julga Processos Éticos e centraliza as informações sobre cursos de Especialização registrados e reconhecidos, bem como sobre o número de inscritos em todo o Brasil, entre Cirurgiões-Dentistas, Auxiliar de Saúde Bucal, Técnicos em Saúde Bucal, Técnicos em Prótese Dentária, Auxiliares de Prótese Dentária e Clínicas Odontológicas.<br /><br />

O Portal do CFO viabiliza o acesso às informações e ações concentradas por esta Autarquia, além de oferecer uma série de serviços online. Isso possibilita o contato mais direto e próximo tanto para a classe odontológica como para a população em geral, que, em última análise, é a razão de existir do CFO e CROs
