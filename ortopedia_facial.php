           O que é Ortopedia Facial?
   			</div>
            
            <div id="texto_site">
            
          		A ortodontia é a especialidade da odontologia que trabalha o posicionamento dos dentes e ossos da boca para regular seu funcionamento. Mas dentro da ortodontia existe também uma área que atua diretamente sob os ossos, músculos e articulações da face. Essa área é a ortopedia facial.<br /><br />

<fontered>Para quê serve?</fontered><br /><br />

A ortopedia facial é usada para corrigir desequilíbrios ósseos e musculares nos maxilares e assim melhorar a eficiência de algumas funções naturais da boca, como a respiração e a mastigação.<br /><br />

Esse tipo de problemas costuma ter duas origens possíveis: fatores genéticos e alterações nos processos da boca, como a falta de estímulo à mastigação e problemas respiratórios.<br /><br />

<fontered>Como funciona?</fontered><br /><br />

A diferença de outras áreas da ortodontia, a ortopedia facial atua nos ossos e não diretamente nos dentes. Por isso, esse tipo de tratamento costuma ser feito com aparelho removíveis e não fixos.<br /><br />

Também pelo fato de mexer com ossos, a ortopedia facial é aplicada geralmente durante a fase de crescimento, já que é mais fácil corrigir o desenvolvimento dos ossos enquanto estão se formando do que depois de formados.<br /><br />

É por isso que o uso de aparelhos em tratamentos de ortopedia facial entre adultos é menos eficiente. Nesses casos, a maioria dos pacientes é encaminhada para uma possível solução através da cirurgia bucomaxilofacial, tendo o aparelho apenas como um complemento para refinar o resultado.
          
