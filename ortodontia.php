           O que é ortodontia?
   			</div>
            
            <div id="texto_site">
            
          		Ortodontia é uma especialidade odontológica que corrige a posição dos dentes e dos ossos maxilares posicionados de forma inadequada. Dentes tortos ou dentes que não se encaixam corretamente são difíceis de serem mantidos limpos, podendo ser perdidos precocemente, devido à deterioração e à doença periodontal. Também causam um estresse adicional aos músculos de mastigação que pode levar a dores de cabeça, síndrome da ATM e dores na região do pescoço, dos ombros e das costas. Os dentes tortos ou mal posicionados também prejudicam a sua aparência.<br /><br />

O tratamento ortodôntico torna a boca mais saudável, proporciona uma aparência mais agradável e dentes com possibilidade de durar a vida toda.<br /><br />

O especialista neste campo é chamado de ortodontista. Os ortodontistas precisam fazer um curso de especialização, além dos cinco anos do curso regular.<br /><br />

<fontered>Como saber se preciso de um ortodontista?</fontered><br /><br />
Apenas seu dentista ou ortodontista poderá determinar se você poderá se beneficiar de um tratamento ortodôntico. <br /><br />

Com base em alguns instrumentos de diagnóstico que incluem um histórico médico e dentário completo, um exame clínico, moldes de gesso de seus dentes e fotografias e radiografias especiais, o ortodontista ou dentista poderá decidir se a ortodontia é recomendável e desenvolver um plano de tratamento adequado para você. Se você apresenta algum dos problemas abaixo, pode ser um candidato para o tratamento ortodôntico:<br /><br />

    => SOBREMORDIDA, algumas vezes chamada de "dentes salientes" — este problema é caracterizado por um excesso vertical da região anterior da maxila e/ou uma sobre-erupção dos dentes dessa região. Nos casos de sobremordida, os dentes anteriores superiores recobrem quase 100% dos dentes inferiores, conferindo um sorriso desagradável e problemas mastigatórios. Os dentes inferiores podem, inclusive, estar tocando no palato e na gengiva do arco superior.<br /><br />
    => MORDIDA CRUZADA ANTERIOR — uma aparência de "bulldog", quando a arcada inferior está projetada muito à frente ou a arcada superior se posiciona muito atrás.<br /><br />
    => MORDIDA CRUZADA — ocorre quando a arcada superior não fica ligeiramente à frente da arcada inferior ao morder normalmente.<br /><br />
    => MORDIDA ABERTA— espaço entre as superfícies de mordida dos dentes anteriores e/ou laterais quando os dentes posteriores se juntam.<br /><br />
    => DESCIO DE LINHA MEDIANA — ocorre quando o centro da arcada superior não está alinhado com o centro da arcada inferior.<br /><br />
    => DIASTEMA — falhas, ou espaços, entre os dentes como resultado de dentes ausentes ou dentes que não preenchem a boca.<br /><br />
    => APINHAMENTO —ocorre quando existem dentes demais para se acomodarem na arcada dentária pequena.<br /><br />
    <fontered>Como funciona um tratamento ortodôntico eficaz?</fontered><br /><br />

Diversos tipos de aparelhos, tanto fixos como móveis, são utilizados para ajudar a movimentar os dentes, retrair os músculos e alterar o crescimento mandibular.<br /><br />

Estes aparelhos funcionam colocando uma leve pressão nos dentes e ossos maxilares. A gravidade do seu problema é que irá determinar qual o procedimento ortodôntico mais adequado e mais eficaz.

          
