            Odontologia domiciliar: uma mudança de paradigma
			</div>
            
                        
            <div id="texto_site">
            A falta de conhecimento dos profissionais e familiares a respeito desse tipo diferenciado de se praticar Odontologia, faz com que muitos problemas decorrentes da cavidade bucal interfiram na saúde geral (sistêmica) na maioria dos pacientes que utilizam esse tipo de serviço individualizado.<br />

Alexandre Franco Miranda *<br /><br />

Realizar a odontologia domiciliar com responsabilidade é fugir dos padrões de formação do cirurgião-dentista, ou seja, o atendimento odontológico a esses pacientes é caracterizado pela adaptação ao seu ambiente com todas as dificuldades possíveis no intuito de proporcionar a saúde como um todo. <br /><br />

O atendimento desses pacientes em domicílio surge como uma odontologia ALTERNATIVA, como mais uma DIFERENCIAÇÃO PROFISSIONAL que o cirurgião-dentista CAPACITADO pode atuar(22).<br /><br />

O maior público alvo desse tipo de atendimento são os idosos (semi e dependentes) em domicílio, pacientes portadores de necessidades especiais impossibilitados de se adaptarem ao consultório e pacientes hospitalizados (UTI, enfermaria) que necessitam da manutenção da saúde bucal como parte integrante da saúde geral(...). <br /><br />

(...)A responsabilidade ética, nesse tipo de atendimento, faz-se necessária e obrigatória. O consentimento livre e esclarecido ao responsável legal e o preenchimento da ficha de desenvolvimento clínico com cada procedimento realizado assinado pelo profissional e pelo responsável, faz com que esse tipo de atendimento seja mais credibilizado pelos familiares e demais profissionais envolvidos. E, o mais importante, é a proteção legal do profissional diante do plano de tratamento elaborado e sua execução.<br /><br />

Os principais problemas de caráter odontológico encontrado nas visitas domiciliares são a presença de placa bacteriana, periodontopatias, perdas dentárias decorrentes de problemas periodontais e da doença cárie, próteses mal adaptadas e higienizadas e lesões decorrentes de trabalhos protéticos mal feitos e ajustados, hábitos deletérios e relacionadas a problemas sistêmicos. As principais áreas de atuação do cirurgião-dentista estão direcionadas à odontologia preventiva, periodontia, prótese, estomatologia(lesões na cavidade bucal) e cirurgia(caráter emergencial), se necessária.<br /><br />

No tratamento odontológico domiciliar deve ser feito APENAS os procedimentos essenciais, por isso a grande importância de um plano de tratamento bem elaborado e em conjunto com os outros profissionais e familiares para o sucesso do tratamento.<br /><br />

A valorização da estética passa a ser de último plano, antecedida pela função necessária ao paciente para desempenhar funções como mastigação e fala, SEMPRE objetivando a eliminação de qualquer tipo de foco de inflamação, infecção e sintomatologia dolorosa no paciente, proporcionando o BEM-ESTAR e QUALIDADE DE VIDA do mesmo!<br /><br />

O atendimento domiciliar, conforme muitos pensam, não necessitam diretamente do uso do consultório portátil. Porém, certos procedimentos necessários nesses pacientes não requerem a presença afinca de equipamentos. Por isso o odontólogo deve estar preparado a se submeter a possíveis adaptações de trabalho, de materiais auxiliares e facilitadores durante o atendimento.<br /><br />

Porém, existem equipamentos odontológicos portáteis que contribuem bastante para a ação clínica do cirurgião-dentista. O odontológo deverá se instruir melhor para o uso de tais equipamentos que apresentam custos menores do que a montagem de um consultório particular, por exemplo.<br /><br />

Uma das maiores dificuldades de implantação das ações odontológicas nessa situação, é a falta de conhecimento específico dos cuidadores, normalmente técnicos de enfermagem e familiares despreparados em relação aos princípios básicos de higienização bucal, controle de placa bacteriana e medidas preventivas para a manutenção da saúde bucal. (...)<br /><br />



