A falta de profissionais da Odontologia capacitados em integrarem o sistema de saúde do paciente assistido em domicílio e ambiente hospitalar (ambulatorial e em tratamento intensivo), determina a odontologia domiciliar como mais uma dimensão para o cirurgião-dentista explorar e vivenciar nos dias de hoje.<br /><br />
